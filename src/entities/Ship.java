package entities;

public class Ship extends Entity {
	/**
	 * Orientation of the ship in radians
	 */
	protected double orientation;
	
	public Ship(int x, int y) {
		super("images/palloncino.png", x, y, true);
		speedX=10;
		speedY=-10;
	}
	public double getSpeedX(){
		return speedX;
	}
	public double getSpeedY(){
		return speedY;
	}
}
